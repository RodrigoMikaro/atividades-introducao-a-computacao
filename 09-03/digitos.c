#include <stdio.h>

int main()
{
  int N;
  scanf("%d", &N);

  while (N > 0) {
    printf("%d\n", N % 10);
    N /= 10; /* equivalente a N = N / 10; */
  }

  // N = 30:
  // -------
  // 30 > 0
  // 30 % 10 = 0 -> N = 30 / 10 = 3
  //--------
  // 3 > 0
  // 3 % 10 = 3 -> N = 3 / 10 = 0
  // ------
  // 0 = 0 -> Fim

  
  // N = 40:
  // ---------
  // 40 > 0
  // 40 % 10 = 0 -> N = 40 / 10 = 4
  // ------
  // 4 > 0
  // 4 % 10 = 4 -> N = 4 / 10 = 0
  // -------
  // 0 = 0 -> Fim

  return 0;
}